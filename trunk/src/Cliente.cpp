	Cliente::Cliente()
	{
		this->id = 0;
		this->fila = 0;
		this->interrompido = 0;
	}
	
    Cliente::Cliente(int pid, int pfila, bool pinterrompido)
	{
        this->id = pid;
        this->fila = pfila;
		this->interrompido = pinterrompido;
    }
	
